export function loadAlphabet(session,promiselist,callback) {
    for (let index = 0; index < filenames.length; index++) {
        const src = 'data/img/alphabet/' + filenames[index] + '.png';
        promiselist.push(callback(src).then((image) => 
        {
            session.alphabet[letters[index]] = image
        }))
    }
}

export function printText(session,txt,position)
{
    const canvas = document.getElementById('canvas');
    const ctx = canvas.getContext('2d');
    for (let index = 0; index < txt.length; index++) 
    {
        if(txt[index] !== ' ')
           ctx.drawImage(session.alphabet[txt[index]], position.x+(8*index), position.y);
    }
}